window.LANG = localStorage.getItem("prosp_lang") || "en";
window.t = function (key) {
  const d = I18N[LANG] || I18N.en;
  return (key in d) ? d[key] : (I18N.en[key] ?? key);
};

function applyI18n() {
  document.documentElement.lang = LANG;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const v = t(el.getAttribute("data-i18n"));
    if (el.hasAttribute("data-i18n-html")) el.innerHTML = v;
    else el.textContent = v;
  });
  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
  const b = document.getElementById("langBtn");
  if (b) b.textContent = LANG === "en" ? "🌐 EN" : "🌐 NL";
  document.dispatchEvent(new CustomEvent("langchange"));
}

function toggleLang() {
  LANG = LANG === "en" ? "nl" : "en";
  window.LANG = LANG;
  localStorage.setItem("prosp_lang", LANG);
  applyI18n();
}

document.addEventListener("DOMContentLoaded", () => {
  applyI18n();
  const b = document.getElementById("langBtn");
  if (b) b.addEventListener("click", toggleLang);
});
